const HOW_TO_ASK_QUESTIONS = `

I was a personal tutor in my youth; I've taught hundreds of students, and at this point in my life I am onboarding new hires and training those early in their career.

I take incredible pride in the people I've taught; there are many people, however, that I wasn't able to help.
The primary differentiator among these students: *knowing when and how to ask for help*.

## Those that don't know when to ask

I'd put these people in two camps:
- Those whose pride precludes them from asking for help.
- Those who take their confusion as a permanent fact about themselves.

### 1) The prideful

At some point they learned that the greats became great on their own. 
You should not need help to become great, because the great do not need help.

This is of course nonsense. Even Ramanujan and Einstein, as prophetic as they are purported to be, needed mentors.

### 2) The pitied

At some point, a well-intentioned teacher or parent saw them struggling with math and declared to them that they were "not a math person," 
"not an English person," "not an athlete."

Do genetic advantages exist? Of course.
But even those plucked from the choicest Darwinian garden still struggle.

In fact, struggling through challenges is a necessary condition for greatness. 

I've had countless students, both 12-year-olds and 20-year-olds, give up the second their brain fails to grok a topic.
Instead of asking for help, they shrug as if God had rolled the dice at their birth and ordained that they would never be able to understand the difference of two perfect squares.

Intellectual achievements require agency, and too many were taught that they do not have agency. 

## Those that don't know how to ask.

The other problematic bucket of students are those that are willing to ask for help but do not do so productively.

As said before, struggling is a necessary condition of intellectual success. 
There are many who use help as a way to avoid struggling, but they should be using help as a way to work through struggle.

In training newly graduated software engineers, this usually comes through the form of an "I'm getting an error, help" message.
In students, it comes as an unprompted "How do I do #2 on the homework?".
Both of these questions indicate that the student has not sufficiently struggled.

I tell students there are 3 reasons they can come to me with questions.

### 1) They've arrived at a supposed contradiction
* "How is it possible that my speed calculation came out negative?"
* "How can tax cuts increase GDP without changing resources or productivity?"
* "Why are my tests passing locally but not in QA?"

Reaching a contradiction proves two important things:
* They are willing to struggle towards understanding.
* They are building a rational model of a topic.

If a 5 minute conversation to resolve the contradiction lets them continue to do those two things, it is worthwhile for all parties.

### 2) You've identified the part you misunderstand and want the expert to teach you
* "Can you walk me through the proof of Taylor's theorem?"
* "Can you explain the short vs long run distinction in the AD/AS model?"
* "Can you show me how the local test runner works?"

Again, being able to identify the specific element of confusion is a sign that you have wrestled enough with a topic to narrow down exactly what you don't understand.
The student could go down the rabbit hole themselves and spend hours reaching an understanding, but that is not necessarily a worthwhile use of their time.

Today, K-12 education is blessed with free online resources to answer these types of questions; college professors use learning management software to publish their lecture notes. 
A teacher explaining a topic for 5 minutes can fill any gaps left by these resources.

In corporate settings, particularly on engineering teams, the understanding of certain systems tends to be locked in the brains of a handful of people.
There is no shame in asking for 5 minutes of their time, if it prevents you from spending 8 hours reading code.

### 3) You need a rubber duck

*Rubber ducking* is a technique in software engineering where you articulate your problem out loud to a rubber duck, because the act of articulating your problem in words often reveals a flaw in your logic.

What's better than a rubber duck? A real human being who can identify the flaw for you. A fresh pair of eyes is invaluable to problem solving.

When a student has struggled and struggled and is not making progress, I want them to approach me and say:
"I've tried a bunch of things and it's not working. Can I walk you through the steps I took and hopefully you see where I'm going wrong?"

Again, this indicates two very important things:
- They are struggling
- They are attempting to build a rational model of the topic

In my experience, no one in their career evolves past needing to ask for help in this way.

If you are in the business of solving hard problems, you will inevitably dig so deep into logical mountains, tainting your eyes with dirt and darkness, that you can't see simple missteps you've made along the way.
`;

